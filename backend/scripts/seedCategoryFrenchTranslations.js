async function findCategoryByPath(path) {
  let parentId = null;
  let currentCategory = null;

  for (const segment of path) {
    currentCategory = await Category.findOne({
      name: segment,
      parent: parentId,
    });

    if (!currentCategory) {
      return null;
    }

    parentId = currentCategory._id;
  }

  return currentCategory;
}

async function seedFrenchCategoryTranslations() {
  await mongoose.connect(process.env.MONGODB_URI, {
    serverSelectionTimeoutMS: 5000,
    connectTimeoutMS: 5000,
    family: 4,
  });

  let updatedCount = 0;
  const missingPaths = [];

  for (const entry of translationEntries) {
    const category = await findCategoryByPath(entry.path);

    if (!category) {
      missingPaths.push(entry.path.join(" > "));
      continue;
    }

    category.translations = category.translations || {};
    category.translations.fr = {
      ...(category.translations.fr || {}),
      ...entry.fr,
    };

    await category.save();
    updatedCount += 1;
    console.log(`updated ${entry.path.join(" > ")} -> ${entry.fr.name}`);
  }

  console.log(`updated_count ${updatedCount}`);

  if (missingPaths.length > 0) {
    console.log("missing_paths");
    for (const path of missingPaths) {
      console.log(`- ${path}`);
    }
  }
}

seedFrenchCategoryTranslations()
  .then(async () => {
    await mongoose.connection.close();
  })
  .catch(async (error) => {
    console.error("seed_failed", error.message);
    try {
      await mongoose.connection.close();
    } catch {}
    process.exit(1);
  });
